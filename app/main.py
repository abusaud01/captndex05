# اسم الملف: app/main.py

from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware

# --------------------------------------------------------------------------
# نستورد كل الأدوات اللازمة من نفس الحزمة (app)
# --------------------------------------------------------------------------
from . import models, routers
from .database import engine

# --------------------------------------------------------------------------
# هذه هي اللحظة الحاسمة: هذا السطر هو الذي يأمر بإنشاء ملف قاعدة البيانات
# والجداول بناءً على المخططات في models.py.
# --------------------------------------------------------------------------
models.Base.metadata.create_all(bind=engine)

# --------------------------------------------------------------------------
# إنشاء كائن التطبيق الرئيسي مع معلومات تعريفية
# --------------------------------------------------------------------------
app = FastAPI(
    title="منصة الكابتن القانوني",
    description="نحن لا ندير القضايا، نحن نهندسها.",
    version="1.0.0-beta"
)

# --------------------------------------------------------------------------
# إعداد سياسة مشاركة الموارد عبر المصادر (CORS)
# هذا السطر ضروري للسماح لملف index.html بالتحدث مع الخادم.
# --------------------------------------------------------------------------
origins = ["*"] # السماح لجميع المصادر للتبسيط في مرحلة التطوير

app.add_middleware(
    CORSMiddleware,
    allow_origins=origins,
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

# --------------------------------------------------------------------------
# تجميع وتركيب جميع "خرائط الطريق" (Routers) في التطبيق الرئيسي
# --------------------------------------------------------------------------
app.include_router(routers.router_users)
app.include_router(routers.router_cases)
app.include_router(routers.router_analysis)

# --------------------------------------------------------------------------
# نقطة نهاية ترحيبية للتأكد من أن الخادم يعمل
# --------------------------------------------------------------------------
@app.get("/", tags=["Root"])
async def read_root():
    return {"message": "مرحباً بك في منصة الكابتن القانوني الهندسية"}
